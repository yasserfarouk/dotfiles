        IFS='|' read -r push_repo rest <<< "$push_entry"
        if [ "$repo" == "$push_repo" ]; then
            is_pushed=true
            break
        fi
    done
    
    # If only behind (not diverged and not pushed), might be external repo
    if ! $is_diverged && ! $is_pushed && [ "$commits" -gt 100 ]; then
